// Get current language from localStorage or default to English
let currentLang = localStorage.getItem('language') || 'en';

// Function to translate the page
function translatePage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            // Check if it's an input placeholder
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update language toggle button text to show CURRENT language
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.textContent = lang === 'en' ? 'ðŸ‡¬ðŸ‡§ EN' : 'ðŸ‡«ðŸ‡® FI';
    }
}

// Function to initialize language toggle button
function initLanguageToggle() {
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        // Remove any existing listeners by cloning the button
        const newLangBtn = langBtn.cloneNode(true);
        langBtn.parentNode.replaceChild(newLangBtn, langBtn);
        
        // Add fresh event listener
        newLangBtn.addEventListener('click', () => {
            const newLang = currentLang === 'en' ? 'fi' : 'en';
            translatePage(newLang);
        });
    }
}

// Initialize translation on page load
document.addEventListener('DOMContentLoaded', () => {
    translatePage(currentLang);
    initLanguageToggle();
});
